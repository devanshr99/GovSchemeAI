async def _seed_schemes(db):
    """Seed 150+ government schemes (central + state-level) with structured eligibility rules."""

    # Get category IDs
    result = await db.execute(select(Category))
    categories = {c.slug: c.id for c in result.scalars().all()}

    schemes_data = _get_schemes_data(categories)
    seen_slugs = set()
    seeded_count = 0

    for scheme_data in schemes_data:
        rules_data = scheme_data.pop("rules", [])
        slug = scheme_data.get("slug")
        if slug in seen_slugs:
            logger.warning(f"Skipping duplicate scheme with slug: {slug}")
            continue
        seen_slugs.add(slug)

        scheme = Scheme(**scheme_data)
        db.add(scheme)
        await db.flush()

        for rule_data in rules_data:
            rule_data["scheme_id"] = scheme.id
            db.add(EligibilityRule(**rule_data))
        seeded_count += 1

    await db.flush()
    logger.info(f"  Seeded {seeded_count} schemes with eligibility rules.")


