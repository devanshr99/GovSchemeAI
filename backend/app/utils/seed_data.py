DATA_DIR = Path(__file__).parent.parent.parent / "data"


async def seed_if_empty():
    """Only seed if the database has no schemes."""
    async with async_session() as db:
        result = await db.execute(select(func.count(Scheme.id)))
        count = result.scalar() or 0
        if count > 0:
            logger.info(f"Database already has {count} schemes. Skipping seed.")
            return

        logger.info("Seeding database with initial data...")
        await _seed_categories(db)
        await _seed_states(db)
        await _seed_districts(db)
        await _seed_schemes(db)
        await db.commit()
        logger.info("Seed complete.")


async def force_reseed():
    """Delete all data and reseed from scratch."""
    from app.models.scheme import EligibilityRule
    from app.models.location import State, District
    from sqlalchemy import delete as sql_delete
    async with async_session() as db:
        await db.execute(sql_delete(EligibilityRule))
        await db.execute(sql_delete(Scheme))
        await db.execute(sql_delete(Category))
        await db.execute(sql_delete(District))
        await db.execute(sql_delete(State))
        await db.commit()
    logger.info("Cleared all data. Reseeding...")
    await seed_if_empty()


